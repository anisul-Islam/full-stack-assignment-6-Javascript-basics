function spelling()
{
    let digit = parseInt(prompt('enter a digit:'));

    if(digit===0)
    {
        alert('Zero');
    }
    else if(digit===1)
    {
        alert('One');
    }
    else if(digit===2)
    {
        alert('Two');
    }
    else if(digit===3)
    {
        alert('Three');
    }
    else if(digit===4)
    {
        alert('Four');
    }
    else if(digit===5)
    {
        alert('Five');
    }
    else if(digit===6)
    {
        alert('Six');
    }
    else if(digit===7)
    {
        alert('Seven');
    }
    else if(digit===8)
    {
        alert('Eight');
    }
    else if(digit===9)
    {
        alert('Nine');
    }
    else
    {
        alert("you didn't enter any digit");
    }
}