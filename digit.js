function digit() {

    let digit = parseInt(prompt("Enter a digit 0-9 : "));

    if (digit === 0) {
        alert(`Entered digit ${digit} is Zero`);
    } else if (digit === 1) {
        alert(`Entered digit ${digit} is One`);
    } else if (digit === 2) {
        alert(`Entered digit ${digit} is Two`);
    } else if (digit === 3) {
        alert(`Entered digit ${digit} is Three`);
    } else if (digit === 4) {
        alert(`Entered digit ${digit} is Four`);
    } else if (digit === 5) {
        alert(`Entered digit ${digit} is Five`);
    } else if (digit === 6) {
        alert(`Entered digit ${digit} is Six`);
    } else if (digit === 7) {
        alert(`Entered digit ${digit} is Seven`);
    } else if (digit === 8) {
        alert(`Entered digit ${digit} is Eight`);
    } else if (digit === 9) {
        alert(`Entered digit ${digit} is Nine`);
    } else {
        alert(`You didn't enter any digit`);
    }

}